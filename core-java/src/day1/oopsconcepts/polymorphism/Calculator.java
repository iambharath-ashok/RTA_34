package day1.oopsconcepts.polymorphism;

public class Calculator {

    int add(int num1, int num2) {
        return num1 + num2;
    }


    int add(int num1, int num2, int num3) {
        return num1 + num2 + num3;
    }

    double add(double num1, double num2) {
        return num1 + num2;
    }


}



